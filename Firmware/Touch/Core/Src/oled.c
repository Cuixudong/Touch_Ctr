//更新显存到LCD
void OLED_Refresh_Gram(void)
{
    uint16_t i,n;
    uint8_t cur_data;
    
    //OLED_CUR_POS_Y = OLED_CUR_POS_Y % ((OLED_PAGE_V - 1) * OLED_H);
    for(i=0; i<8; i++)
    {
        OLED_WR_Byte (0xb0+i,OLED_CMD);    //设置页地址（0~7）
        OLED_WR_Byte (0x00,OLED_CMD);      //设置显示位置―列低地址    //1.3寸oled 0x02
        OLED_WR_Byte (0x10,OLED_CMD);      //设置显示位置―列高地址
        for(n=0; n<OLED_W; n++)
        {
            if(OLED_MOVE_DIR == 0)
            {
                if((OLED_CUR_POS_Y % 8) == 0)
                {
                    OLED_WR_Byte(OLED_GRAM[(n + OLED_CUR_POS_X) % (OLED_W * OLED_PAGE_H)][(i + OLED_CUR_POS_Y / 8) % (OLED_PAGE_V * 8)],OLED_DATA);
                }
                else
                {
                    cur_data = (((OLED_GRAM[(n + OLED_CUR_POS_X) % (OLED_W * OLED_PAGE_H)][(i + OLED_CUR_POS_Y / 8 + 1) % (OLED_PAGE_V * 8)] << 8) | \
                    OLED_GRAM[(n + OLED_CUR_POS_X) % (OLED_W * OLED_PAGE_H)][(i + OLED_CUR_POS_Y / 8) % (OLED_PAGE_V * 8)]) >> (OLED_CUR_POS_Y % 8)) & 0xFF;
//cur_data  = OLED_GRAM[n][i + OLED_CUR_POS_Y / 8] >> (OLED_CUR_POS_Y % 8) + (OLED_GRAM[n][i + OLED_CUR_POS_Y / 8 + 1] & oled_mem_calc_arr[OLED_CUR_POS_Y % 8]) << (OLED_CUR_POS_Y % 8);
                    OLED_WR_Byte(cur_data,OLED_DATA);
                }
            }
            else
            {
                OLED_WR_Byte(OLED_GRAM[(n + OLED_CUR_POS_Y) % (OLED_W * OLED_PAGE_H)][i + ((OLED_CUR_POS_Y + n) / (OLED_W * OLED_PAGE_H)) * 8],OLED_DATA);
            }
        }
    }
}

//SPI 速度设置函数
void SPI2_SetSpeed(uint8_t SpeedSet)
{
    assert_param(IS_SPI_BAUDRATE_PRESCALER(SpeedSet));//判断有效性
    __HAL_SPI_DISABLE(&hspi2);              //关闭SPI
    hspi2.Instance->CR1&=0XFFC7;            //位3-5清零，用来设置波特率
    hspi2.Instance->CR1|=SpeedSet;          //设置SPI速度
    __HAL_SPI_ENABLE(&hspi2);               //使能SPI
}

//SPIx 读写一个字节
uint8_t SPI2_ReadWriteByte(uint8_t TxData)
{
    uint8_t  Rxdata;
    HAL_SPI_TransmitReceive(&hspi2,&TxData,&Rxdata,1, 1000);
    return Rxdata;  //返回收到的数据
}

//向SSD1306写入一个字节。
//dat:要写入的数据/命令
//cmd:数据/命令标志 0,表示命令;1,表示数据;
void OLED_WR_Byte(uint8_t dat,uint8_t cmd)
{
    uint8_t i;
    if(cmd) //写命令
    {
        OLED_DC_H;
    }
    else
    {
        OLED_DC_L;
    }
    OLED_CS_L;
    #if (!USB_HW_SPI)
    for(i=0;i<8;i++)
    {
        OLED_D0_L;
    
        if(dat&0x80)
            OLED_D1_H;
        else 
            OLED_D1_L;
    
        OLED_D0_H;
    
        dat<<=1;
    }
    #else
    i = dat;
    HAL_SPI_Transmit(&hspi2,&i,1,0xFF);
    #endif
    OLED_CS_H;
    OLED_DC_H;
}

//开启OLED显示
void OLED_Display_On(void)
{
    OLED_WR_Byte(0X8D,OLED_CMD);  //SET DCDC命令
    OLED_WR_Byte(0X14,OLED_CMD);  //DCDC ON
    OLED_WR_Byte(0XAF,OLED_CMD);  //DISPLAY ON
}
//关闭OLED显示
void OLED_Display_Off(void)
{
    OLED_WR_Byte(0X8D,OLED_CMD);  //SET DCDC命令
    OLED_WR_Byte(0X10,OLED_CMD);  //DCDC OFF
    OLED_WR_Byte(0XAE,OLED_CMD);  //DISPLAY OFF
}

