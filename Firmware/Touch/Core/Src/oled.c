//p_h:图片高（单位像素）
//*p:图片起始地址
void OLED_Show_Picture(uint16_t x,uint16_t y,uint8_t *p,uint16_t p_w,uint16_t p_h)
{
    uint8_t temp, i, col, row;
    uint8_t y0 = y;
    uint8_t high,width;
    uint8_t exp_col_bytes,act_col_bytes;
    width = p_w;
    if(x + p_w > OLED_W)
    {
        width = OLED_W - p_w;//实际显示宽度
    }
    high = p_h;
    if(y + p_h > OLED_H)
    {
        high = OLED_H - p_h;//实际显示高度
    }
    exp_col_bytes = (p_h / 8 + ((p_h % 8) ? 1 : 0));//显示一列的字节数
    act_col_bytes = (high / 8 + ((high % 8) ? 1 : 0));//实际显示一列的字节数

    for(row = 0; row < width; row++) //列++
    {
        for(col = 0; col < act_col_bytes; col++) //显示一列
        {
            temp = p[col + row * exp_col_bytes];
            for(i=0; i<8; i++)
            {
                if(temp&0x80)
                    OLED_DrawPoint(x,y,1);
                else 
                    OLED_DrawPoint(x,y,0);
                temp <<= 1;
                y++;
                if((y - y0) == high)
                {
                    y = y0;
                    x++;
                    break;
                }
            }
        }
    }
}

